package vn.mobileid.GoPaperless.model.fpsModel;

public class Alignment {
    private  String alignment;
    private String horizontal_alignment;
    private String vertical_alignment;


    public String getAlignment() {
        return alignment;
    }

    public void setAlignment(String alignment) {
        this.alignment = alignment;
    }

    public String getHorizontal_alignment() {
        return horizontal_alignment;
    }

    public void setHorizontal_alignment(String horizontal_alignment) {
        this.horizontal_alignment = horizontal_alignment;
    }

    public String getVertical_alignment() {
        return vertical_alignment;
    }

    public void setVertical_alignment(String vertical_alignment) {
        this.vertical_alignment = vertical_alignment;
    }
}
